let x =30;
let y =40;

//   == operator
console.log(x==y);

//   === operator
 console.log(x==y);

//   != operator
console.log(x!=y);

//   !== operator
console.log(x!=y);

//   > operator
console.log(x>y);

//   < operator
console.log(x<y);

//   >= operator
console.log(x>=y);

//   <= operator
console.log(x<=y);

//   ? operator (ternary)
console.log(x>y ? "x is greater than y" : "y is greater than x");
 